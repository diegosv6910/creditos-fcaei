package vistas;

public class Creditos {
    int ID_Creditos;
    int Matricula;
    int ID_Evento;
    int ID_Log_Evidencia;
    String Tipo_Credito;
    public static String M_Busqueda;

    public Creditos() {
        
    }

    public int getID_Creditos() {
        return ID_Creditos;
    }

    public void setID_Creditos(int ID_Creditos) {
        this.ID_Creditos = ID_Creditos;
    }

    public int getMatricula() {
        return Matricula;
    }

    public void setMatricula(int Matricula) {
        this.Matricula = Matricula;
    }

    public int getID_Evento() {
        return ID_Evento;
    }

    public void setID_Evento(int ID_Evento) {
        this.ID_Evento = ID_Evento;
    }

    public int getID_Log_Evidencia() {
        return ID_Log_Evidencia;
    }

    public void setID_Log_Evidencia(int ID_Log_Evidencia) {
        this.ID_Log_Evidencia = ID_Log_Evidencia;
    }

    public String getTipo_Credito() {
        return Tipo_Credito;
    }

    public void setTipo_Credito(String Tipo_Credito) {
        this.Tipo_Credito = Tipo_Credito;
    }

    public String getM_Busqueda() {
        return M_Busqueda;
    }

    public void setM_Busqueda(String M_Busqueda) {
        this.M_Busqueda = M_Busqueda;
    }

        
    
}
